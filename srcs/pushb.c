#include "../include/push_swap.h"

void	ft_pushb(t_data *dta)
{
	int	l;

	l = 0;
	while (dta->taba[0][l])
	{
		dta->taba[0][l] = dta->tabb[0][l];
		l++;
	}
	dta->taba[0][l] = '\0';
}
